//Los módulos de NodeJS se excluyen de la compilación de TypeScript

//Por otra parte, es posible ignorar archivos de nuestra elección
//agregando en el archivo tsconfig.json la propiedad "exclude" como se muestra a continuación: 

//   "exclude": [
//     "node_modulesIgnore"
//   ]
// }

//Alternativamente, podemos usar la propiedad "include" para indicarle a TypeScript qué archivos debe incluir en la compilación.
//por ejemplo, si queremos que TypeScript compile todos los archivos .ts que se encuentren en la carpeta "src", 
//podemos agregar la siguiente propiedad en el archivo tsconfig.json:

//   "include": [
//     "src/**/*.ts"
//]